          Navigator.pushReplacement(
            context,
            MaterialPageRoute(
              builder: (_) => userDoc.exists
                  ? const HomeScreen()
                  : const InputBirthdayScreen(),
            ),
          );
        }
      }
    } catch (e) {
      if (kDebugMode) {
        print("Google Sign-Up Error: $e");
      }

      setState(() => _isLoading = false);

      if (!mounted) return;

